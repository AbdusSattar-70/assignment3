function kilometerToMeter(getKilometer) {
    if(getKilometer != 0 || getKilometer == 0){
        // 1 kilometer = 1000 meter. user input (getKilometer) parsing to float
        var meter = +getKilometer * 1000; 
    }
    return meter;
}

